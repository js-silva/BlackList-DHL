package blacklist.webservice;


public class DhlBlackListException extends Exception {


	/**
	 *
	 */
	private static final long serialVersionUID = 1L;


	public DhlBlackListException() {
		super();
	}

	public DhlBlackListException(String arg0) {
		super(arg0);
	}

	public DhlBlackListException(Throwable arg0) {
		super(arg0);
	}

	public DhlBlackListException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public DhlBlackListException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
