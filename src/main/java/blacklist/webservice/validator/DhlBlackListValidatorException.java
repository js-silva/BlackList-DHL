package blacklist.webservice.validator;


public class DhlBlackListValidatorException extends Exception {


	/**
	 *
	 */
	private static final long serialVersionUID = 1L;


	public DhlBlackListValidatorException() {
		super();
	}

	public DhlBlackListValidatorException(String arg0) {
		super(arg0);
	}

	public DhlBlackListValidatorException(Throwable arg0) {
		super(arg0);
	}

	public DhlBlackListValidatorException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public DhlBlackListValidatorException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
